public class GuardaVolumes {
        Integer listaPecas;
        Integer nomesPecas;

    public GuardaVolumes(Integer listaPecas, Integer nomesPecas) {
        this.listaPecas = listaPecas;
        this.nomesPecas = nomesPecas;
    }

    public Integer getListaPecas() {
        return listaPecas;
    }

    public void setListaPecas(Integer listaPecas) {
        this.listaPecas = listaPecas;
    }

    public Integer getNomesPecas() {
        return nomesPecas;
    }

    public void setNomesPecas(Integer nomesPecas) {
        this.nomesPecas = nomesPecas;
    }
}
